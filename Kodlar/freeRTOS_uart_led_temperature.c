/**
  * @brief  Function implementing the uart_task thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_uart_task_func */
void uart_task_func(void *argument)
{
  /* USER CODE BEGIN 5 */
	typedef union uart_com{
	uint8_t uart_buffer_int[10];
	char uart_buffer_char[10];
	}uart_com;
	
	uart_com msg;
	
  uint8_t send_flag = 0;
  
	/* Infinite loop */
  for(;;)
  {
		HAL_UART_Receive (&huart2,msg.uart_buffer_int, 6, 200);
		if (strncmp(msg.uart_buffer_char,"ON",2) == 0){
			send_flag = 1;																										//SET FLAG ON
		}
		if (strncmp(msg.uart_buffer_char,"OFF",3) == 0){
			send_flag = 0;																										//SET FLAG OFF
			TIM3 -> CCR1 = 100; 																							//RESET THE LED
		}
		if (send_flag == 1){
			osEventFlagsSet(temp_eventHandle, 0x0001U);                        // set the flag 0x0001U for event sig1_id
			osDelay(1);
		}else{
			osDelay(1);
		}
		msg.uart_buffer_int[0] = 0;
		msg.uart_buffer_int[1] = 0;
		msg.uart_buffer_int[2] = 0;
		msg.uart_buffer_int[3] = 0;
		msg.uart_buffer_int[4] = 0;
		msg.uart_buffer_int[5] = 0;
  }
  /* USER CODE END 5 */
}

