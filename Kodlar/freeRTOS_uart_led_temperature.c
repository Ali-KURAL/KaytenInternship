/**
* @brief Function implementing the led_task thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_led_task_func */
void led_task_func(void *argument)
{
  /* USER CODE BEGIN led_task_func */
  volatile int16_t adc_val;
  volatile float temp;
  /* Infinite loop */
  for(;;)
  {
		osEventFlagsWait(temp_eventHandle, 0x0001U, osFlagsWaitAny, osWaitForever);
		osEventFlagsClear(temp_eventHandle,0x0001U);
		HAL_ADC_Start(&hadc);
		if(HAL_ADC_PollForConversion(&hadc,1) == HAL_OK)
		{
			adc_val = HAL_ADC_GetValue(&hadc);	
			temp = (adc_val * 0.0805664); // ((3.3 / 4096)/0.01) * adc_val
		//temp = (adc_val * 0.1220703125);
		}
		char uart_buffer[50];
    sprintf(uart_buffer, "Temperature : %.2f \r\n",temp);
		HAL_UART_Transmit(&huart2,(uint8_t*)uart_buffer,22,100);
		volatile int8_t ccr_val = round(temp - 27.0);
		ccr_val = ccr_val *15;
		if (ccr_val > 100){
			ccr_val = 100;
		}
		if (ccr_val < 0){
			ccr_val = 0;
		}
		TIM3 -> CCR1 = 100-ccr_val;

    osDelay(5);
  }
  /* USER CODE END led_task_func */
}

