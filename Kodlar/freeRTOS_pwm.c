/**
  * @brief  Function implementing the uart_read thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_uart_read_func */
void uart_read_func(void *argument)
{
  /* USER CODE BEGIN 5 */
	typedef union uart_com{
  uint8_t uart_buffer_int[10];
  char uart_buffer_char[10];
	}uart_com;
		
	uart_com msg;
  /* Infinite loop */
  for(;;)
  {

		HAL_UART_Receive (&huart2,msg.uart_buffer_int, 10, 1000);
		if ((msg.uart_buffer_char[4] == 'B') && (msg.uart_buffer_char[0] == 'R')){
			red = msg.uart_buffer_char[1] - '0';
			green =  msg.uart_buffer_char[3] - '0';
			blue =  msg.uart_buffer_char[5] - '0';
			osEventFlagsSet(led_open_eventHandle, 0x0001U);
			HAL_UART_Transmit(&huart2,(uint8_t*)"LED SET\r\n",10,100);
		}
		msg.uart_buffer_int[0] = 0;
		msg.uart_buffer_int[1] = 0;
		msg.uart_buffer_int[2] = 0;
		msg.uart_buffer_int[3] = 0;
		msg.uart_buffer_int[4] = 0;
		msg.uart_buffer_int[5] = 0;
		osDelay(200);
	
  }
  /* USER CODE END 5 */
}

